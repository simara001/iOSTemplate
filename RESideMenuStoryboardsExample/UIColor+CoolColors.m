#import "UIColor+CoolColors.h"

@implementation UIColor (CoolColors)

+ (UIColor *) coolBlue {
    // Hexadecimal #36B9EF
    return [UIColor colorWithRed:0.212 green:0.725 blue:0.941 alpha:1.0];
}
+ (UIColor *) coolGreen {
    // Hexadecimal #3DD1BA
    return [UIColor colorWithRed:0.239 green:0.816 blue:0.729 alpha:1.0];
}
+ (UIColor *) coolYellow {
    return [UIColor colorWithRed:0.984 green:0.694 blue:0.137 alpha:1.0];
}
+ (UIColor *) coolRed {
    // Hexadecimal #F8494D
    return [UIColor colorWithRed:0.973 green:0.286 blue:0.302 alpha:1.0];
}
+ (UIColor *) coolGray {
    return [UIColor colorWithRed:0.906 green:0.906 blue:0.906 alpha:1.0];
}
+ (UIColor *) coolDarkBlue {
    //
    return [UIColor colorWithRed:0.212 green:0.275 blue:0.325 alpha:1.0];
}
+ (UIColor *) coolPurple {
    // Hexadecimal #8C42E2
    return [UIColor colorWithRed:140./255 green:66./255. blue:226./255. alpha:1.0];
}

@end
